const getItems = () => {
    const items = [];
    phones.forEach( (phone, index)=>{
        items.push({
            ...phone,
            id: "phone-"+index,
            category: "phone"
        })
    });
    tablets.forEach( (tablet, index)=>{
        items.push({
            ...tablet,
            id: "tablet-"+index,
            category: "tablet"
        })
    });
    return items;
};

const getItem = (itemId) =>{
    return getItems().find(item => item.id === itemId);
};

